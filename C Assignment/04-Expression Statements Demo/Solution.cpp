#include <stdio.h>

int main() {
    // Declaration statements
    int a, b;
    float c;

    // Assignment statements
    a = 15;
    b = 4;
    c = 2.5;

    // Expression statements (computations + printing)
    printf("Sum: %d\n", a + b);
    printf("Product: %d\n", a * b);
    printf("Result: %.2f\n", a * c);

    return 0;
}
